package com.codestates.seb.calculator;

public class op {

    double plus(double num1, double num2){
        return num1 + num2;
    }
    double minus(double num1,double num2){
        return num1 -num2;
    }
    double multiple(double num1, double num2){
        return num1 * num2;
    }
    double division(double num1, double num2){
        return num1 /num2;
    }
}
